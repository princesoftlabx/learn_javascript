const month = "march"

switch (month) {
    case "jan":
        console.log("January")
        break;
    case "feb":
        console.log("feb")
        break;
    case "march":
        console.log("March")
        break;    //break is used to break control flow
    case "april":
        console.log("April")
        break;

    default:
        console.log("this is default")
        break;
}